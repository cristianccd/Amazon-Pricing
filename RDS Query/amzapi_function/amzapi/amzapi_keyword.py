class KeywordEntry:
  def __init__(self, keywordId, adGroupId, campaignId, kwd_name, kwd_match, kwd_status, curr_bid, max_bid, min_bid, sugg_bid):
    self.keywordId = keywordId
    self.adGroupId = adGroupId
    self.campaignId = campaignId
    self.kwd_name = kwd_name
    self.kwd_match = kwd_match
    self.kwd_status = kwd_status
    self.curr_bid = curr_bid
    self.max_bid = max_bid
    self.min_bid = min_bid
    self.sugg_bid = sugg_bid